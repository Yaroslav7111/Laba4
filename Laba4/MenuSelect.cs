    public static class MenuSelect
    {
        // Метод для отображения горизонтального меню и получения выбранного пункта
        // title - заголовок меню
        // options - массив строк с вариантами выбора
        // x, y - координаты для отображения меню
        public static string ShowHorizontal(string title, string[] options, int x, int y)
        {
            // Изначально выбран первый пункт
            int selected = 0;
            // Клавиша, которую нажимает пользователь
            ConsoleKey key;
           //меню работает бесконечно, пока пользователь не нажмёт Enter
            while (true)
            {
                // заголовок
                Console.SetCursorPosition(x, y);
                Text.PLine(title);
                
                // рисуем горизонтально варианты меню 
                // offsetX - это переменная для смещения каждого пункта меню вправо
                // мы начинаем с координаты x, а затем для каждого пункта добавляем его длину и немного отступа
                // так мы получаем красивое горизонтальное расположение пунктов меню
                // И дай бог чтобы я не запутался в этих отступах , потому что я уже запутался , но вроде работает нормально
                int offsetX = x;

                for (int i = 0; i < options.Length; i++)
                {
                    // устанавливаем курсор для каждого пункта меню
                    Console.SetCursorPosition(offsetX, y + 2);
                     // добавляем пробелы вокруг текста для лучшей видимости
                    string text = " " + options[i] + " ";
                   // выделяем выбранный пункт другим цветом
                    if (i == selected)
                    {
                        // сохраняем текущие цвета консоли
                        Console.ForegroundColor = ConsoleColor.Black;
                        // устанавливаем белый фон для выделения
                        Console.BackgroundColor = ConsoleColor.White;
                        Text.PLine($"[{text}]");
                        /* 
                        восстанавливаем цвета консоли
                        это нужно для того , чтобы остальные пункты меню отображались в обычных цветах , а не в цветах выделенного пункта
                        и да , я знаю что это выглядит ужасно , но я не знаю как это сделать по другому 
                        Слава Императору
                        */
                        Console.ResetColor();
                    }
                    // остальные пункты отображаем обычным цветом
                    else
                    {
                        // просто отображаем пункт меню без выделения
                        Text.PLine($" {text} ");
                    }
                    
                    offsetX += text.Length + 3; // смещение вправо
                }

                key = Console.ReadKey(true).Key;

                switch (key)
                {
                    case ConsoleKey.LeftArrow:
                        selected--;
                        if (selected < 0)
                            selected = options.Length - 1;
                        break;

                    case ConsoleKey.RightArrow:
                        selected++;
                        if (selected >= options.Length)
                            selected = 0;
                        break;

                    case ConsoleKey.Enter:
                        return options[selected];
                }
            }
        }
    }
}